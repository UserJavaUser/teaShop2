package by.htp.ishop.entity;

import by.htp.ishop.entity.CartItem;

public class CartItemView extends CartItem{

	private static final long serialVersionUID = 9179804714992990664L;

	private String name;
	private double price;

	public CartItemView() {
		
	}

	public CartItemView(CartItem cartItem, String name, double price) {
		super(cartItem.getId(), cartItem.getItem(), cartItem.getQuantity(), cartItem.getCart());
		this.name = name;
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		long temp;
		temp = Double.doubleToLongBits(price);
		result = prime * result + (int) (temp ^ (temp >>> 32));
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		CartItemView other = (CartItemView) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (Double.doubleToLongBits(price) != Double.doubleToLongBits(other.price))
			return false;
		return true;
	}

}
